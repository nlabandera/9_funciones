import java.util.*;

public class E931 {
	
public static double cubo(double num) {
	
	double cuboDe= Math.pow(num, 3.0);
	return cuboDe;
	
}
	public static void main(String[] args) {
		
		System.out.println("El cubo de 3.2 es: "+ cubo(3.2));
		System.out.println("El cubo de 5 es: "+ cubo(5));
		

	}

}
